    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        try{
            PreparedStatement consulta = conexion.prepareStatement(""
                    + "SELECT * FROM usuarios WHERE usuario=? AND contrasena=?"
                    + " AND status=1");             
            consulta.setString(1,txtUsuario.getText());
            consulta.setString(2,txtContrasena.getText());
            ResultSet rs = consulta.executeQuery();
            if(rs.next()){
                JOptionPane.showMessageDialog(null, "Bienvenido Al Sistema X");
                MDIPrincipal mdiPrincipal = new MDIPrincipal();
                mdiPrincipal.setExtendedState(JFrame.MAXIMIZED_BOTH);
                mdiPrincipal.setVisible(true);
                
            }else{
                JOptionPane.showMessageDialog(null, "Usuario No Existe");
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

