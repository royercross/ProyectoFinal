    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String apellido_paterno = txtApellidoPaterno.getText();
        String apellido_materno = txtApellidoMaterno.getText();
        String email = txtEmail.getText();
        String usuario = txtUsuario.getText();
        String password = txtPassword.getText();
        
        if (nombre.length() == 0 || apellido_paterno.length() == 0 
                || apellido_materno.length() == 0 || email.length() == 0 
                || usuario.length() == 0 || password.length() == 0)
        {
                JOptionPane.showMessageDialog(null, "Debes llenar todos los campos para registrar.");
                return;
        }
        try{            
           PreparedStatement consulta = conexion.prepareStatement(""
                    + "INSERT INTO usuarios(nombre,apellido_paterno,apellido_materno"
                   + ",email,usuario,contrasena) VALUES (?,?,?,?,?,?)");                  
           consulta.setString(1, nombre);
           consulta.setString(2, apellido_paterno);
           consulta.setString(3, apellido_materno);
           consulta.setString(4, email);
           consulta.setString(5, usuario);
           consulta.setString(6, password);   
           
           
           consulta.executeUpdate();
           
           llenaTablaUsuarios();
           
           limpiaCampos();
           
        }catch(Exception e){
            e.printStackTrace();       
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

